package com.sample;


public class Class {

  private long classId;
  private String className;
  private long classSpecialityId;
  private long classHeadTeacherId;
  private String classNum;
  private long classSpecialityTeacherId;


  public long getClassId() {
    return classId;
  }

  public void setClassId(long classId) {
    this.classId = classId;
  }


  public String getClassName() {
    return className;
  }

  public void setClassName(String className) {
    this.className = className;
  }


  public long getClassSpecialityId() {
    return classSpecialityId;
  }

  public void setClassSpecialityId(long classSpecialityId) {
    this.classSpecialityId = classSpecialityId;
  }


  public long getClassHeadTeacherId() {
    return classHeadTeacherId;
  }

  public void setClassHeadTeacherId(long classHeadTeacherId) {
    this.classHeadTeacherId = classHeadTeacherId;
  }


  public String getClassNum() {
    return classNum;
  }

  public void setClassNum(String classNum) {
    this.classNum = classNum;
  }


  public long getClassSpecialityTeacherId() {
    return classSpecialityTeacherId;
  }

  public void setClassSpecialityTeacherId(long classSpecialityTeacherId) {
    this.classSpecialityTeacherId = classSpecialityTeacherId;
  }

}
