package com.LeaveSystem.model;


public class Jurisdiction {

  private long juscId;
  private String juscNames;
  private String jurisdictions;


  public long getJuscId() {
    return juscId;
  }

  public void setJuscId(long juscId) {
    this.juscId = juscId;
  }


  public String getJuscNames() {
    return juscNames;
  }

  public void setJuscNames(String juscNames) {
    this.juscNames = juscNames;
  }


  public String getJurisdictions() {
    return jurisdictions;
  }

  public void setJurisdictions(String jurisdictions) {
    this.jurisdictions = jurisdictions;
  }

}
