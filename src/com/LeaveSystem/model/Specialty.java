package com.sample;


public class Specialty {

  private long specialtyId;
  private String specialtyName;
  private long specialtyCollegeId;
  private String specialtyNum;


  public long getSpecialtyId() {
    return specialtyId;
  }

  public void setSpecialtyId(long specialtyId) {
    this.specialtyId = specialtyId;
  }


  public String getSpecialtyName() {
    return specialtyName;
  }

  public void setSpecialtyName(String specialtyName) {
    this.specialtyName = specialtyName;
  }


  public long getSpecialtyCollegeId() {
    return specialtyCollegeId;
  }

  public void setSpecialtyCollegeId(long specialtyCollegeId) {
    this.specialtyCollegeId = specialtyCollegeId;
  }


  public String getSpecialtyNum() {
    return specialtyNum;
  }

  public void setSpecialtyNum(String specialtyNum) {
    this.specialtyNum = specialtyNum;
  }

}
