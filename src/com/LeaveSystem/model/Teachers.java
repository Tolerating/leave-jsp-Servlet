package com.LeaveSystem.model;


public class Teachers {

  private long teacherId;
  private String teacherNum;
  private String openId;
  private String teacherPass;
  private String teacherName;
  private String teacherSex;
  private String teacherTel;
  private String post;
  private String teacherIdCard;
  private String teacherApprover;
  private String teacherApprovalResult;
  private java.sql.Timestamp teacherApprovalTime;


  public long getTeacherId() {
    return teacherId;
  }

  public void setTeacherId(long teacherId) {
    this.teacherId = teacherId;
  }


  public String getTeacherNum() {
    return teacherNum;
  }

  public void setTeacherNum(String teacherNum) {
    this.teacherNum = teacherNum;
  }


  public String getOpenId() {
    return openId;
  }

  public void setOpenId(String openId) {
    this.openId = openId;
  }


  public String getTeacherPass() {
    return teacherPass;
  }

  public void setTeacherPass(String teacherPass) {
    this.teacherPass = teacherPass;
  }


  public String getTeacherName() {
    return teacherName;
  }

  public void setTeacherName(String teacherName) {
    this.teacherName = teacherName;
  }


  public String getTeacherSex() {
    return teacherSex;
  }

  public void setTeacherSex(String teacherSex) {
    this.teacherSex = teacherSex;
  }


  public String getTeacherTel() {
    return teacherTel;
  }

  public void setTeacherTel(String teacherTel) {
    this.teacherTel = teacherTel;
  }


  public String getPost() {
    return post;
  }

  public void setPost(String post) {
    this.post = post;
  }


  public String getTeacherIdCard() {
    return teacherIdCard;
  }

  public void setTeacherIdCard(String teacherIdCard) {
    this.teacherIdCard = teacherIdCard;
  }


  public String getTeacherApprover() {
    return teacherApprover;
  }

  public void setTeacherApprover(String teacherApprover) {
    this.teacherApprover = teacherApprover;
  }


  public String getTeacherApprovalResult() {
    return teacherApprovalResult;
  }

  public void setTeacherApprovalResult(String teacherApprovalResult) {
    this.teacherApprovalResult = teacherApprovalResult;
  }


  public java.sql.Timestamp getTeacherApprovalTime() {
    return teacherApprovalTime;
  }

  public void setTeacherApprovalTime(java.sql.Timestamp teacherApprovalTime) {
    this.teacherApprovalTime = teacherApprovalTime;
  }

}
